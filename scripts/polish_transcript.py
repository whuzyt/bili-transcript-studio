def should_start_paragraph(line: str) -> bool:
    return line.startswith(PARAGRAPH_STARTS) or line in {"好。", "好吧。"}


def flush(paragraph: list[str], out: list[str]) -> None:
    if paragraph:
        out.append("".join(paragraph))
        paragraph.clear()


def main() -> None:
    raw_lines = SOURCE.read_text(encoding="utf-8").splitlines()
    out: list[str] = ["# 六年级上册数学沪教版同步教学逐字稿（修订版）", ""]
    paragraph: list[str] = []

    for raw in raw_lines:
        line = raw.strip()
        if not line:
            continue

        heading = re.match(r"^##\s+(p\d{2})\b", line)
        if heading:
            flush(paragraph, out)
            if out and out[-1] != "":
                out.append("")
            out.append(f"## {CHAPTER_TITLES[heading.group(1)]}")
            out.append("")
            continue

        if line in LINE_REPLACEMENTS:
            line = LINE_REPLACEMENTS[line]
        fixed = polish_sentence(punctuate(fix_text(line)))
        if not fixed:
            continue

        if paragraph and (
            paragraph[-1] in {"好。", "好吧。"}
            or should_start_paragraph(fixed)
            or sum(map(len, paragraph)) >= 180
        ):
            flush(paragraph, out)
            out.append("")
        paragraph.append(fixed)

    flush(paragraph, out)
    TARGET.write_text("\n".join(out).strip() + "\n", encoding="utf-8")
    print(TARGET)


if __name__ == "__main__":
    main()
