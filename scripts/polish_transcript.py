def fix_text(text: str) -> str:
    fixed = text
    for pattern, repl in REPLACEMENTS:
        fixed = re.sub(pattern, repl, fixed)
    fixed = re.sub(r"([0-9])\s+([0-9])", r"\1、\2", fixed)
    fixed = re.sub(r"([A-Za-z])\s+([A-Za-z])", r"\1、\2", fixed)
    fixed = re.sub(r"\s+", " ", fixed).strip()
    return fixed


def is_question(line: str) -> bool:
    if line.endswith(("？", "?", "吗")):
        return True
    if any(token in line for token in ("请问", "什么叫", "为什么", "怎么办", "如何", "怎么")):
        return True
    if any(line.endswith(token) for token in QUESTION_PATTERNS):
        return True
    return False


def punctuate(line: str) -> str:
    line = line.strip()
    if not line:
        return ""
    if line.endswith(("。", "？", "！", "：", "；", "）", "】")):
        return line
    if line in {"好", "好吧", "拜拜"}:
        return line + "。"
    if line in {"第一个", "第二个", "第三个", "第四个", "最后一个"}:
        return line + "："
    if line.endswith(("对不对", "是不是", "有没有", "能不能", "可不可以", "清楚吗", "明白吗")):
        return line + "？"
    if is_question(line):
        return line + "？"
    if line.endswith(("呀", "啊", "吧", "哦", "了", "的", "呢", "嘛")):
        return line + "。"
    return line + "。"


